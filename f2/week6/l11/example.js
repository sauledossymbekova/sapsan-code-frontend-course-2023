/* операторы сравнения */
// TRUE
console.log(10 == 10, '10 == 10');

// TRUE // не строгое сравнение
console.log(10 == "10", '10 == "10"');

// FALSE
console.log(10 <= 0, '10 <= 0');

// FALSE
console.log(10 < 0, "10 < 0");

// TRUE
console.log(10 > 0, "10 > 0");

// TRUE ?
console.log(10 >= 0, "10>=0   true????");

// FALSE
console.log(10 == "Saule", "10 == Saule");

// строгое сравнение
console.log(11 === "11", "11 === '11'");

// возвращает обратное от ==
console.log(11 != 'text', "11 != 'text'")

// TRUE
console.log(11 == "11");

// FALSE нестрогое сравнение
console.log(11 != "11");

// FALSE
console.log(11 === "11", "11 === '11'");

// True
// строгое сравнение
console.log(11 !== "11", "11 !== '11'");


